/** Bodies orbiting the Sun or Earth that reuse the Moon rendering pipeline
 * (dwarf planets, ISS, JWST, Parker Solar Probe). */
export const SUN_AND_EARTH_ORBITERS: CelestialBody[] = [...DWARF_PLANETS, ...SPACECRAFT];

export const ALL_BODIES: CelestialBody[] = [
  SUN,
  ...PLANETS,
  ...MOONS,
  ...SUN_AND_EARTH_ORBITERS,
  ...PROBES,
  ...COMETS,
];

export function getBodyById(id: string): CelestialBody | undefined {
  return ALL_BODIES.find((b) => b.id === id);
}

export function getMoonsOf(planetId: string): CelestialBody[] {
  return [...MOONS, ...SUN_AND_EARTH_ORBITERS].filter((m) => m.parentId === planetId);
}
