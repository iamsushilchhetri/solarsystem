/** Fills in diameter/surfaceArea/volume from radius so the raw data below only needs radiusKm. */
function withGeometry(
  b: Omit<CelestialBody, 'diameterKm' | 'surfaceAreaKm2' | 'volumeKm3'>,
): CelestialBody {
  const r = b.radiusKm;
  return {
    ...b,
    diameterKm: r * 2,
    surfaceAreaKm2: 4 * Math.PI * r * r,
    volumeKm3: (4 / 3) * Math.PI * r * r * r,
  };
}

